package com.iastate.eventXpert.enums;

/**
 * A DateTime Picker that allows various behavior based on what field is actually selected
 * <p>
 * START_DATE_PICKER: the start date of the event
 * END_DATE_PICKER: the end date of the event
 * START_TIME_PICKER: the start time of the event
 * END_TIME_PICKER: the end time of the event
 */
public enum DateTimePicker {
	START_DATE_PICKER,
	END_DATE_PICKER,
	START_TIME_PICKER,
	END_TIME_PICKER
}
